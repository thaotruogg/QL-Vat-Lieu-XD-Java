    private void jbtnLuuHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLuuHDActionPerformed
        listGuest = khDLL.getAllKhachHang();
        String soHD = jtxtMaHD.getText();
        int rowKH = jcbxMaKH.getSelectedIndex();
        String idKH = listGuest.get(rowKH).getMaKH();
        String ngayHD = jdateChooserHD.getText();
        
        if(jtxtMaHD.getText().length() == 0 || jtxtMaHD.getText().equals("Nhập số hóa đơn")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số hóa đơn");
        }
        else if(jdateChooserHD.getText().length() == 0){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn ngày");
        }
        else{
            Bill bill = new Bill();
            bill.setSoHD(soHD);
            bill.setMaKH(idKH);
            bill.setNgayHD(ngayHD);
            if(hdDLL.AddBill(bill)){
                JOptionPane.showMessageDialog(this, "Thêm hóa đơn thành công!");
                this.setVisible(false);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Thêm hóa đơn thất bại!");
            }
        }
    }//GEN-LAST:event_jbtnLuuHDActionPerformed

